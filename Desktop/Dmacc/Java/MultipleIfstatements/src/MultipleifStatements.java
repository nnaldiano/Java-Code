
public class MultipleifStatements {

	public static void main(String[] args) {
		int n = 2;
		if (n > 10) {
		       System.out.print("*****");
		} else if (n > 7) { 

		       System.out.print("****");
		} else if (n > 4) { 

		       System.out.print("***");
		} else if (n > 1) { 

		       System.out.print("**");
		}

		System.out.println("*");

	}

}
